# Tipos int e float 
#int -> Número inteiro
#O tipo int representa qualquer número
#positivo e negativo. int sem sinal é considerado
#positivo
print(11) #int
print(-11) #int
print(0) #inteiro

#float -> Numero com ponto flutuante

print(1.1, 10.11)
print(0.0, -1.5)

#Função type mostra o tipo que o Python

print(type('Lailson')) 
print(type(1)) 
print(type(1.1), type(1-1), type(0.0)) 